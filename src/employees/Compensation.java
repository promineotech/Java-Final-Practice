package employees;

public class Compensation {
	
	private double salary;
	private int ptoHoursAccrued;
	
	public double getSalary() {
		return salary;
	}
	
	public void setSalary(double salary) {
		this.salary = salary;
	}
	
	public int getPtoHoursAccrued() {
		return ptoHoursAccrued;
	}
	
	public void setPtoHoursAccrued(int ptoHoursAccrued) {
		this.ptoHoursAccrued = ptoHoursAccrued;
	}
}
